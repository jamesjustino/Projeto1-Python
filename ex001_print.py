'''
--------------- PRIMEIROS COMANDOS EM PYTHON -------------------

Comandos usados nessa aula:
print(), input(), msg =, nome = input()
'''

print('====== Exercício 001 - print() ======')

print('Olá, Mundo!')                # comando para escrever algo na tela.

msg='Olá, mundo!'                   # variável recebendo uma string obs: a string fica entre aspas ''
print(msg)                          # mostrando o conteúdo dessa variável na tela

nome = input('Qual o seu nome?')    # variável recebendo um comando input() que vai solicitar uma informação ao usuário
print('Seu nome é',nome)            # mostrando essa informação solicitada na tela junto com um texto. Obs: use vírgula para incluir a variável
